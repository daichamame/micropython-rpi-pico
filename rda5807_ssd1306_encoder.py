# RDA5807の初期化
radio = daichamame_rda5807.RDA5807(ch=0,scl_pin=5,sda_pin=4,frequency=100000)
radio.init()
radio.set_volume(vol)		# ボリュームの設定
radio.set_freq(freq)        # 周波数の設定
