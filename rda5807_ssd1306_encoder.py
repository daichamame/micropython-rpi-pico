while 1:
    re_status=re.read_position()
    if re_status[1] != current_pos: # 現在の値から変更されたら設定
        radio.set_freq(re_status[1]/10)
        ssd1306.print(32,16,"{:5.1f}".format(re_status[1]/10),2)       # 周波数表示更新
        ssd1306.display()
        current_pos=re_status[1]
    
